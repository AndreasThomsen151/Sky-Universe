Citizen.CreateThread(function()
    while true do
		Citizen.Wait(1000)
		local player = GetPlayerPed(-1)
		if IsPedInAnyVehicle(player, false) and not IsPauseMenuActive() then
		bilskade = GetVehicleEngineHealth(GetVehiclePedIsIn(player,false))
				bilskaden = bilskade/10
								SendNUIMessage({
									showCarMenu = true,
									stamina = bilskaden,
												})		
			else
								SendNUIMessage({
									showCarMenu = false,
												})
			end
	end
end)

